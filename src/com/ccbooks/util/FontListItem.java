package com.ccbooks.util;

public class FontListItem{
	private String ttfFileName;
	private String ttfFilePath;

	
	public FontListItem() {
		
	}

	public String getTtfFileName() {
		return ttfFileName;
	}

	public void setTtfFileName(String ttfFileName) {
		this.ttfFileName = ttfFileName;
	}

	public String getTtfFilePath() {
		return ttfFilePath;
	}

	public void setTtfFilePath(String ttfFilePath) {
		this.ttfFilePath = ttfFilePath;
	}

}   